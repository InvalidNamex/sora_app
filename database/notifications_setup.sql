-- 1) device_tokens hardening
do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'device_tokens'
      and column_name = 'id'
      and is_identity = 'NO'
  ) then
    execute 'alter table public.device_tokens alter column id add generated by default as identity';
  end if;
end
$$;

